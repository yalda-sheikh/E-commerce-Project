public class VariantRequest {

    private int itemId;

    private String color;
    private double price;
    private int stock;

    public int getItemId() {
        return itemId;
    }

    public String getColor() {
        return color;
    }

    public double getPrice() {
        return price;
    }

    public int getStock() {
        return stock;
    }
}